package br.com.api.gerenciamento.dto;

import br.com.api.gerenciamento.model.Endereco;

public class EnderecoResponseDTO {

	private String logradouro;
	private String numero;
	private String complemento;
	private String bairro;
	private String cep;
	private String cidade;
	private String estado;
	private Integer id_cliente;
	
	public static Endereco toModel(EnderecoResponseDTO enderecoResponseDTO) {
		Endereco endereco = new Endereco();
		endereco.setLogradouro(enderecoResponseDTO.getLogradouro());
		endereco.setNumero(enderecoResponseDTO.getNumero());
		endereco.setCep(enderecoResponseDTO.getCep());
		endereco.setBairro(enderecoResponseDTO.getBairro());
		
		return endereco;
	}

	public String getLogradouro() {
		return logradouro;
	}

	public void setLogradouro(String logradouro) {
		this.logradouro = logradouro;
	}

	public String getNumero() {
		return numero;
	}

	public void setNumero(String numero) {
		this.numero = numero;
	}

	public String getComplemento() {
		return complemento;
	}

	public void setComplemento(String complemento) {
		this.complemento = complemento;
	}

	public String getBairro() {
		return bairro;
	}

	public void setBairro(String bairro) {
		this.bairro = bairro;
	}

	public String getCep() {
		return cep;
	}

	public void setCep(String cep) {
		this.cep = cep;
	}

	public String getCidade() {
		return cidade;
	}

	public void setCidade(String cidade) {
		this.cidade = cidade;
	}

	public String getEstado() {
		return estado;
	}

	public void setEstado(String estado) {
		this.estado = estado;
	}

	public Integer getId_cliente() {
		return id_cliente;
	}

	public void setId_cliente(Integer id_cliente) {
		this.id_cliente = id_cliente;
	}

}
